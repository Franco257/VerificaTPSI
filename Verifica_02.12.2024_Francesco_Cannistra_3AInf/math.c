int somma (int A, int B){
    int risultato= A+B;
    return risultato;
}

int sotrazione (int A, int B){
    int risultato= A-B;
    return risultato;
}

int moltiplicazione (int A, int B){
    int risultato= A*B;
    return risultato;
}

float divisione (int A, int B){
    float risultato= A/B;
    return risultato;
}